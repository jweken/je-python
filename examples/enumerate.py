seasons = ["Spring", "Summer", "Autumn", "Winter"]

print('\nExample of enumerate (builtin)', __file__, '\n')

# start the list with zero
print('start a list with zero:\n',
      'list(enumerate(seasons)\n', list(enumerate(seasons)))
print()
# start the list with any number
print('start a list with any number:\n',
      'list(enumerate(seasons, 109))\n', list(enumerate(seasons, 109)))
